export type SmartReviewTranslationKey = keyof typeof TRANSLATIONS.en;

export function resolveSmartReviewLocale(languageSetting: SmartReviewLanguageSetting = "auto"): SmartReviewLocale {
  const language = languageSetting === "auto" ? getLanguage() : languageSetting;
  return language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function t(
  locale: SmartReviewLocale,
  key: SmartReviewTranslationKey,
  values: Record<string, string | number> = {}
): string {
  let text: string = TRANSLATIONS[locale][key] ?? TRANSLATIONS.en[key];
  for (const [name, value] of Object.entries(values)) {
    text = text.split(`{${name}}`).join(String(value));
  }
  return text;
}
